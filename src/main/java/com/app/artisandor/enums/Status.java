package com.app.artisandor.enums;

/**
 * This enum represents the different status values that an entity can have.
 *
 * @author Anas FANANI
 * @version 1.0
 */
public enum Status {
    /**
     * Represents an active status.
     */
    ACTIVE,

    /**
     * Represents an inactive status.
     */
    INACTIVE,

    /**
     * Represents a suspended status.
     */
    SUSPENDED
}
